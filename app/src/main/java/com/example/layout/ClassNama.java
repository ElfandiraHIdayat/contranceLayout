package com.example.layout;

public class ClassNama {
    private String Nama;

    //inialisasi objek
    public ClassNama(String nama)
    {
        this.Nama = nama;
    }

    //Membuat  methon getName untuk mengembalikan nilai ke variabel Nama
    public String getName()
    {
        return this.Nama;
    }
}
